        public static string OldAlgorithm( ReadOnlySpan<byte> source)
        {
            var sb = new StringBuilder(source.Length);
            foreach (var character in source)
            {
                sb.Append((char) character);
            }

            return sb.ToString();
        }

        [Benchmark(Baseline = true)]
        public string OldAlgorithm() => OldAlgorithm(source);
        [Benchmark]
        public string NewAlgorithm() => source.ExtendedAsciiString();
